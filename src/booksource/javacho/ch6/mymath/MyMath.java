package booksource.javacho.ch6.mymath;

public class MyMath {
    long add(long a, long b) {
        return a + b;
    }
    long sub(long a, long b) {
        return a - b;
    }
    long mul(long a, long b) {
        return a * b;
    }
    double div(double a, double b) {
        return a/b;
    }
}
